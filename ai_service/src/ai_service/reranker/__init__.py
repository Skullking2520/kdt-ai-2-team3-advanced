"""
RAG 시스템에서 리랭커(Reranker)는 1차 검색(Retriever)된 방대한 문서 중에서 
질문과 가장 관련성이 높은 핵심 문서만 골라 최상단으로 재정렬하는 후처리 기술입니다.

1. Reranker가 필요한 이유

의미론적 정확도 향상: 임베딩 기반의 1차 검색은 속도가 빠르지만 단어의 미묘한 차이나 
질문의 복잡한 의도를 정확히 파악하기 어렵습니다. 
리랭커는 질문과 문서 간의 관계를 직접 비교 분석하여 더 정밀한 연관성을 찾아냅니다.
LLM의 '중실종(Lost in the Middle)' 현상 방지: 
LLM은 주어지는 프롬프트(컨텍스트)가 길어지면 가운데에 있는 정보를 잘 잊어버립니다. 

리랭커를 통해 관련성 높은 문서를 프롬프트 가장 위쪽에 배치하면 
LLM이 핵심을 놓치지 않고 정확한 답변을 생성할 수 있습니다.

속도와 정확성의 조화: 검색 속도가 느린 모델로 모든 문서를 검색하면 비효율적입니다. 
먼저 빠르고 가벼운 모델로 후보군을 대량 추출(Retriever)한 뒤, 
정확한 모델로 순위를 매기는(Reranker) 2단계 구조를 사용하면 성능과 효율을 
모두 잡을 수 있습니다.

2. 필수적인 요소일까?

반드시 필수는 아닙니다. 

RAG 시스템의 목적과 환경에 따라 선택할 수 있습니다.

필수적인 경우 (Advanced RAG):금융, 의료, 법률처럼 팩트의 정확성이 최우선시되는 도메인

방대한 양의 문서 풀(Pool)에서 검색해야 하는 경우

Lost in the Middle'을 방지하여 환각 현상을 최소화해야 하는 경우

필수적이지 않은 경우 (Naive RAG):
사내 규정집처럼 전체 문서의 양이 비교적 적은 경우
짧은 Q&A 위주의 단순한 검색 환경
시스템 응답 속도가 최우선 과제인 경우
"""

